#!/usr/bin/env node
/**
 * Build-time code generator.
 *
 * Reads every file under templates/<name>/ and writes
 * convex/workflows/templateFiles.ts with their contents embedded as string
 * literals.  This is necessary because Convex actions run in a bundled Node.js
 * environment that cannot read files from disk at runtime.
 *
 * Usage:
 *   node scripts/embed-templates.mjs
 *
 * Run this before every `npx convex deploy` (handled by package.json scripts).
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const TEMPLATES_DIR = path.join(ROOT, "templates");
const OUTPUT_FILE = path.join(ROOT, "convex", "workflows", "templateFiles.ts");

// Files that need the executable bit set in the git tree (mode 100755).
const EXECUTABLE_FILES = new Set(["set-convex-env.sh"]);

function walkDir(dir, base = "") {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries.sort((a, b) => a.name.localeCompare(b.name))) {
    const relPath = base ? `${base}/${entry.name}` : entry.name;
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...walkDir(fullPath, relPath));
    } else if (entry.isFile()) {
      files.push({ relPath, fullPath });
    }
  }
  return files;
}

const templates = {};
for (const templateName of fs.readdirSync(TEMPLATES_DIR).sort()) {
  const templateDir = path.join(TEMPLATES_DIR, templateName);
  if (!fs.statSync(templateDir).isDirectory()) continue;

  const files = walkDir(templateDir);
  templates[templateName] = files.map(({ relPath, fullPath }) => {
    const content = fs.readFileSync(fullPath, "utf-8");
    const fileName = path.basename(relPath);
    const executable = EXECUTABLE_FILES.has(fileName);
    return { path: relPath, content, executable };
  });
}

// Build the TypeScript source
const lines = [
  "// AUTO-GENERATED by scripts/embed-templates.mjs — do not edit manually.",
  "// Run: node scripts/embed-templates.mjs",
  "",
  "export interface TemplateFile {",
  "  /** Relative path inside the repo (e.g. 'app/page.tsx') */",
  "  path: string;",
  "  /** UTF-8 file content */",
  "  content: string;",
  "  /** Whether the file should be marked executable (git mode 100755) */",
  "  executable: boolean;",
  "}",
  "",
  "export const TEMPLATES: Record<string, TemplateFile[]> = {",
];

for (const [templateName, files] of Object.entries(templates)) {
  lines.push(`  ${JSON.stringify(templateName)}: [`);
  for (const file of files) {
    lines.push(
      `    { path: ${JSON.stringify(file.path)}, executable: ${file.executable}, content: ${JSON.stringify(file.content)} },`,
    );
  }
  lines.push("  ],");
}

lines.push("};", "");

fs.writeFileSync(OUTPUT_FILE, lines.join("\n"), "utf-8");

const totalFiles = Object.values(templates).reduce(
  (sum, files) => sum + files.length,
  0,
);
console.log(
  `✓ Generated ${path.relative(ROOT, OUTPUT_FILE)} (${Object.keys(templates).length} template(s), ${totalFiles} files total)`,
);
for (const [name, files] of Object.entries(templates)) {
  console.log(`  ${name}: ${files.length} files`);
}
